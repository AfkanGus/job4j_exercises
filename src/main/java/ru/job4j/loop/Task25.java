package ru.job4j.loop;

/**
 * 25. Вывести числа в арифметической прогрессии по возрастанию
 * Ниже представлен метод loop(int num), который принимает целое положительное число - количество членов арифметической прогрессии.
 * <p>
 * Пусть первый член прогрессии равен 7, а разница между соседними членами прогрессии равна 4 (в возрастающем порядке).
 * <p>
 * Задание: Написать код, который выводит в консоль через пробел члены прогрессии в количестве равном входящему числу.
 * <p>
 * Примечание: для сборки результирующей строки с выводом значений через пробел нужно использовать StringJoiner.
 * <p>
 * Например, для числа 5 вывод будет:
 * <p>
 * 7 11 15 19 23
 */
public class Task25 {
    public static void loop(int num) {
        int firstTerm = 7; // Первый член прогрессии
        int commonDifference = 4; // Разница между членами прогрессии

        StringBuilder result = new StringBuilder();
        for (int i = 0; i < num; i++) {
            result.append(firstTerm).append(" ");
            firstTerm += commonDifference; // Увеличиваем первый член на разницу
        }

        System.out.println(result.toString().trim()); // Выводим результат
    }
}
