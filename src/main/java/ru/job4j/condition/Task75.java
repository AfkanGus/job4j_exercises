package ru.job4j.condition;

/**
 * 76. Количество повторений "внутреннего" числа
 * Ниже представлен метод innerNum(int num2, int num4), который принимает 2 целых числа - num2 - двузначное, num4 - четырехзначное.
 * <p>
 * Задание: Написать код, который определяет и выводит в консоль количество раз, когда двузначное число фигурирует как "внутреннее" по отношению к
 * четырехзначному. То есть какая-то пара смежных цифр в четырехзначном числе образует входящее двузначное число от 0 до 3 раз.
 * <p>
 * Например, для чисел 33 и 3331 вывод будет:
 * <p>
 * 2
 */
public class Task75 {
    public static void innerNum(int num2, int num4) {
        // Преобразуем двузначное число в строку для сравнения
        String num2Str = String.valueOf(num2);
        // Преобразуем четырехзначное число в строку для обхода цифр
        String num4Str = String.valueOf(num4);

        int count = 0; // Счетчик совпадений

        // Перебираем все пары смежных цифр в четырехзначном числе
        for (int i = 0; i < num4Str.length() - 1; i++) {
            // Формируем пару смежных цифр из четырехзначного числа
            String pair = num4Str.substring(i, i + 2);
            // Если пара смежных цифр равна двузначному числу, увеличиваем счетчик
            if (pair.equals(num2Str)) {
                count++;
            }
        }

        // Выводим количество совпадений в консоль
        System.out.println(count);
    }
}
