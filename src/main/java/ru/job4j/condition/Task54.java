package ru.job4j.condition;

/**
 * 53. Преобразовать трехзначное число в зависимости от условия
 * Ниже представлен метод transform(int number), который принимает целое трехзначное число.
 * <p>
 * Задание: Написать код, который выполняет следующее:
 * <p>
 * - Если число четное - все четные цифры этого числа увеличиваются на 1, а нечетные - уменьшаются на 1. Если
 * увеличение/уменьшение невозможно (например, увеличить 9), то оставить эту цифру без изменения.
 * <p>
 * - Если число нечетное, то все изменения произвести наоборот - четные цифры уменьшить на 1, нечетные - увеличить на 1.
 * <p>
 * Получившееся число вывести в консоль.
 * <p>
 * Например, для числа 309 вывод будет:
 * <p>
 * 409
 */
public class Task54 {
    public static void transform(int number) {
        int sign = number < 0 ? -1 : 1; // Запоминаем знак числа

        number = Math.abs(number); // Работаем с положительным числом

        int multiplier = 1; // Множитель для позиции цифры (единицы, десятки, сотни)

        int result = 0;

        while (number > 0) {
            int digit = number % 10; // Получаем последнюю цифру числа
            int modifiedDigit;

            if (number % 2 == 0) {
                // Если число четное, увеличиваем четные цифры, уменьшаем нечетные
                if (digit % 2 == 0) {
                    modifiedDigit = (digit + 1) % 10;
                } else {
                    modifiedDigit = (digit - 1 + 10) % 10;
                }
            } else {
                // Если число нечетное, уменьшаем четные цифры, увеличиваем нечетные
                if (digit % 2 == 0) {
                    modifiedDigit = (digit - 1 + 10) % 10;
                } else {
                    modifiedDigit = (digit + 1) % 10;
                }
            }

            result += modifiedDigit * multiplier; // Обновляем результат
            multiplier *= 10; // Увеличиваем множитель
            number /= 10; // Убираем последнюю цифру
        }

        number = result * sign; // Учитываем знак и обновляем переданное число
        System.out.println(number);
    }

}

