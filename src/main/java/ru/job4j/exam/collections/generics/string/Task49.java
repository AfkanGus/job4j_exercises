package ru.job4j.exam.collections.generics.string;

/**
 * 1. Является ли число симметричным (палиндромом).
 * Ниже представлен метод isPalindrome(int number),
 * который принимает целое положительное число (не больше 99999).
 * <p>
 * Симметричным (палиндромом) называется число, которое
 * дает одинаковое значение при чтении его справа налево и слева
 * направо. Например, 696.
 * <p>
 * Задание: Написать код, который проверяет, является ли входящее
 * число симметричным (палиндромом), и выводит в
 * консоль "Да", если является, и "Нет", если не является.
 * <p>
 * Примечание: упражнение нужно решить путём приведения числа к
 * типу String и дальше применять метод charAt(). Это
 * не самое эффективное решение, здесь мы просто набиваем практику.
 * Эффективное решение этого упражнения будет
 * разобрано в следующей задаче.
 * <p>
 * Например, вывод для числа 22 будет:
 * <p>
 * Да
 */
public class Task49 {
    public static void isPalindrome(int number) {
        // Преобразовываем число в строку
        String numStr = Integer.toString(number);

        // Получаем длину строки
        int length = numStr.length();

        // Переменная для хранения результата сравнения
        boolean isPalindrome = true;

        // Сравниваем символы справа налево и слева направо
        for (int i = 0; i < length / 2; i++) {
            if (numStr.charAt(i) != numStr.charAt(length - 1 - i)) {
                isPalindrome = false;
                break;
            }
        }

        // Выводим результат в консоль
        if (isPalindrome) {
            System.out.println("Да");
        } else {
            System.out.println("Нет");
        }
    }
}
